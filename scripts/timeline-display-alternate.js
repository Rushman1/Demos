function getTestDate(days = 0, months = 0, years = 0) {
  let newDate = new Date();
  newDate.setDate(newDate.getDate() + days);
  newDate.setDate(newDate.getMonth() + months);
  newDate.setDate(newDate.getFullYear() + years);

  return newDate;
}
